	<p>AVID’s curriculum, resources, methodologies, and strategies have always been research-based. AVID Elementary (AE) is steeped in research and practice from internationally recognized thought leaders, in subjects like motivation, student achievement and influence. Below is a brief overview of AE’s research basis.</p>

	<p>AE is immersed in the research of Dr. Carol Dweck, one of the world’s leading researchers in the field of motivation. In her book Mindset: The New Psychology of Success,  she says:</p>

	<p><em>“In the growth mindset, people believe that their talents and abilities can be developed through passion, education, and persistence. For them, it’s not about looking smart of grooming their image. It’s about a commitment to learning—taking informed risks and learning from the results, surrounding yourself with people who will challenge you to grow, looking frankly at your deficiencies and seeking to remedy them."</em></p>

