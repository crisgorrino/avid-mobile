	<h2>AVID College Readiness System</h2>
	<p>The AVID College Readiness System is implemented at the <a href="">elementary</a>, <a href="">secondary</a> and <a href="">higher education</a> levels. Though it operates as a system, each part may be implemented independently depending on a district’s needs.</p>

	<p><a href="">AVID Elementary</a> (K‐6, K‐8) teaches students fundamental learning, study and academic behavioral skills. It is a foundational component of the AVID System designed to be embedded into the daily instruction of all elementary classrooms, including combined K‐8 schools. AVID’s strategies and philosophy of educational opportunities for all students are threaded throughout the instructional day, across grade levels, and ultimately across sites.</p>

	<a href="">Learn more</a>

	<p><a href="">AVID Secondary</a> (grades 7‐12) develops learning, study and academic behavioral skills that are essential to success in rigorous coursework. It acts as a catalyst for schools to develop a culture of college readiness for all students across the campus. In the AVID Elective class, students receive daily instruction and support to prepare them for college from a trained AVID Elective teacher. AVID impacts students schoolwide as academic strategies like writing to learn, inquiry, collaboration, organizational skills, and critical reading (WICOR) are taught in all classes by teachers who have been trained to use AVID strategies in their specific content area.</p>

	<a href="">Learn more</a>

	<p><a href="">AVID for Higher Education</a> is comprised of three initiatives: Student Success Initiative, Teacher Preparation Initiative and the Career and Technical Workforce Initiative. AVID for Higher Education works with postsecondary institutions to support students with the goal of increasing academic success, persistence, and completion rates in the Student Success Initiative. Colleges of education use the AVID Teacher Preparations Initiative to provide teacher candidates with AVID frameworks, methodologies, and strategies so that they enter the teaching field able to meet a broad spectrum of students’ needs. The Career and Technical Education (CTE) Initiative collaborates with colleges to increase student persistence and completion of degree and certificate programs, and help graduates be successful in the workplace.</p>

	<a href="">Learn more</a>
	
	<h2>Career Readiness</h2>
	<p>It is estimated that by the year 2020, two‐thirds of all jobs will require some level of postsecondary education. AVID prepares students for 21st century careers each day by providing the academic and behavioral skills necessary to succeed in the workplace. Organization, timemanagement, critical thinking, collaboration and problem‐solving are strong components of the AVID System. AVID includes career exploration in its secondary curriculum, develops partnerships with organizations to help students find <a href="">pathways to careers</a>, and supports and prepares students in degree and certificate programs to be <a href="">successful in the workforce.</a></p>
