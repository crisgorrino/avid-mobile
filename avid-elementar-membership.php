	<h2>AVID Elementary Roles</h2>
	<p>AVID Elementary Liaison:</p>
	<p>The AE Liaison is required to attend <span>AVID Summer Institute</span> with AE participants and complete a two-year training cycle in AVID Elementary Leadership Training held by AVID Center. This role also approves data and site certification submissions to AVID Center.</p>

	<p>AVID Elementary Principal:</p>
	<p>The AE Principal is required to attend <span>AVID Summer Institute</span> with their Site Team(s) and is the instructional leader for AE Implementation and the AE Site Team. The AE Principal may attend AE Leadership Training with the AE Liaison. This role is responsible for leading the AE Site Team throughout the certification process and submits data and the certification document to the AE Liaison.</p>

	<p>AVID Elementary Teacher:</p>
	<p>The AE Teacher is required to attend the appropriate implementation stage strand at <span>AVID Summer Institute</span> and is the facilitator of learning in the classroom. This role is responsible for conducting classroom and individual assessments in a pre/post model to utilize data to inform classroom instruction. This role is an active member on the AE Site Team.</p>

	<h2>AVID Elementary Professional Learning</h2>
	<p>AVID Elementary Liaison Leadership Training Sessions<br>
	The Elementary Liaison attends two Leadership training sessions during Year 1 and two sessions during Year 2. The designated Elementary Liaison also attends the AVID Summer Institute in sequential elementary strands. Additional AE Liaisons and AE Site Principals can also attend AE Leadership Training at a discounted rate to build capacity within implementing sites.</p>

	<p><a href="">AVID Elementary Liaison Leadership Trainings Informational Document</a href=""></p>

	<p>AVID Summer Institute (Elementary Strands)<br>
	Implementing sites attend AVID Elementary Implementation strands according to the implementation stage of AE (Beginnings, Foundations, or Bridges). Attendance is required the first two years of implementation. A Minimum team of four members per implementing site is required.</p>
	
	<p>Who attends:<br>
	- Site Principal is required<br>
	- Implementing Teachers (SPED and General Ed)<br>
	- Instructional Coaches/Specialists</p>

