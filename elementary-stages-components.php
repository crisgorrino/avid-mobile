	AVID Elementary leadership supports, guides, and facilitates AVID Elementary implementation for all students.</p>

	<p>Essential Four: Systems<br>
	AVID Elementary sites align their systems through the use of the Four Pillars of Excellence (accountability, articulation, assessment, and calibration) to ensure the fidelity of AVID Elementary implementation across all grades.</p>

	<h2>Components</h2>
	<p>During each stage, AVID Elementary incorporates:</p>
	<p>Student Success Skills – encompassing communication skills (e.g. listening, speaking, writing), self-advocacy skills, and study skills.</p>
	<p>Organizational Skills – both mental and physical. Students are given organizational tools, time management, goal setting and note-taking strategies.</p>
	<p>WICOR Lessons and L‐WICOR Lessons – emphasize instruction on writing to learn, inquiry, collaboration, organization and reading to learn in all content areas.</p>
	<p>Partnerships – among students, classrooms, grade levels, schools, feeder patterns, families and communities.</p>

